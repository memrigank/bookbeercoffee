class mathsOperations{

static float sum(double x, double y){
return (float)(x+y);
}

static int sum(int x, int y){
return x+y;
}

static float diff(float x, float y){
return x-y;
}

static int mod(int x, int y){
return x%y;
}

static float div(float x, float y){
return x/y;
}
}